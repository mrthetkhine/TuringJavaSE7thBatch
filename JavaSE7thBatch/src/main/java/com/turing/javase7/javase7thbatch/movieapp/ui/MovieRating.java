    void addRating()
    {
        User user = this.authService.getAuthUser();
        int row = this.tblMovies.getSelectedRow();
        if(row !=-1)
        {
            Integer movieId = (Integer)this.tblMovies.getModel().getValueAt(row, 0);
            int rating = Integer.parseInt(this.cboRating.getSelectedItem().toString());
            
            this.ratingService.addRating(user.id(), movieId, rating);
            this.loadRatingForUser(user.id());
        }
    }
