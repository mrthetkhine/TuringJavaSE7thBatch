    void loadMovies()
    {
        ArrayList<Movie> movies = this.movieService.getAllMovie();
        System.out.println("Movie size "+movies.size());
        DefaultTableModel model = (DefaultTableModel)this.tblMovies.getModel();
        
        model.setRowCount(0);
        
        for(Movie movie : movies)
        {
            Object[]row = new Object[4];
            row[0] = movie.id();
            row[1] =movie.title();
            row[2] = movie.year();
            row[3] = this.getGenresForMovie(movie.id());
            
            model.addRow(row);
        }
    }
    public void loadRatingForUser(int userId)
    {
        ArrayList<Rating> ratings = this.ratingService.getAllRatingByUser(userId);
        System.out.println("Movie size "+ratings.size());
        DefaultTableModel model = (DefaultTableModel)this.tblMovieRating.getModel();
        
        model.setRowCount(0);
        
        for(Rating rating : ratings)
        {
            Object[]row = new Object[4];
            row[0] = rating.id();
            row[1] =rating.userId();
            row[2] = rating.movieId();
            row[3] = rating.rating();
            
            model.addRow(row);
        }
    }
    String getGenresForMovie(int movieId)
    {
        ArrayList<Genre> genres = this.genreService.getAllGenresByMovieId(movieId);
        String str = "";
        
        for(Genre genre : genres)
        {
            if(str.length()>0)
            {
                str += ", "+ genre.name();
            }
            else
            {
                str = genre.name();
            }
        }
        return str;
    }
