    void addActor()
    {
        String name = this.txtName.getText();
        String gender = this.cboGender.getSelectedItem().toString();
        Date dob = Date.from(this.selectedDate.atStartOfDay(ZoneId.systemDefault()).toInstant());
        
        Actor actor = new Actor(0,name,gender,dob);
        this.actorService.saveActor(actor);
        
        this.loadAllActors();
        this.clearForm();
    }
    void clearForm()
    {
        this.txtName.setText("");
        this.cboGender.setSelectedItem("");
        this.txtDob.setText("");
        this.selectedDate = null;
                
    }
