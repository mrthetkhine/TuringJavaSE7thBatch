    void deleteActor()
    {
        int row = this.tblActors.getSelectedRow();
        if(row !=-1)
        {
            int result = JOptionPane.showConfirmDialog(null, "Delete", "Are you sure you want to delete", JOptionPane.YES_NO_OPTION);
            if(result == JOptionPane.YES_OPTION)
            {
                Integer id = (Integer)this.tblActors.getModel().getValueAt(row, 0);
                
                this.actorService.deleteActorById(id);
                
                this.loadAllActors();
            }
        }
    }
