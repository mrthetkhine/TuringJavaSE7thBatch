    void loadGenresToComboBox()
    {
        ArrayList<Genre> genres = this.genreService.getAllGenres();
        for(Genre genre : genres)
        {
            this.cboGenres.addItem(genre.name());
            this.genereAndId.put(genre.name(), genre.id());
        }
    }
    void loadMovies()
    {
        ArrayList<Movie> movies = this.movieService.getAllMovie();
        System.out.println("Movie size "+movies.size());
        DefaultTableModel model = (DefaultTableModel)this.tblMovies.getModel();
        
        model.setRowCount(0);
        
        for(Movie movie : movies)
        {
            Object[]row = new Object[4];
            row[0] = movie.id();
            row[1] =movie.title();
            row[2] = movie.year();
            row[3] = this.getGenresForMovie(movie.id());
            
            model.addRow(row);
        }
    }
    String getGenresForMovie(int movieId)
    {
        ArrayList<Genre> genres = this.genreService.getAllGenresByMovieId(movieId);
        String str = "";
        
        for(Genre genre : genres)
        {
            if(str.length()>0)
            {
                str += ", "+ genre.name();
            }
            else
            {
                str = genre.name();
            }
        }
        return str;
    }
