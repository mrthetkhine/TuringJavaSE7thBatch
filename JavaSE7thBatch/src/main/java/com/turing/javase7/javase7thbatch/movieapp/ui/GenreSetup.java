    void loadAllGenre()
    {
        ArrayList<Genre> genres = this.genreService.getAllGenres();
        System.out.println("Genre size "+genres.size());
        DefaultTableModel model = (DefaultTableModel)this.tblGenres.getModel();
        
        model.setRowCount(0);
        
        for(Genre genre : genres)
        {
            Object[]row = new Object[2];
            row[0] = genre.id();
            row[1] =genre.name();
           
            
            model.addRow(row);
        }
    }
    void addGenre()
    {
        String name = this.txtGenre.getText();
        this.genreService.addGenre(name);
        this.loadAllGenre();
        
        this.txtGenre.setText("");
    }
