    void deleteGenre()
    {
        int row = this.tblGenres.getSelectedRow();
        if(row !=-1)
        {
            int result = JOptionPane.showConfirmDialog(null, "Delete", "Are you sure you want to delete", JOptionPane.YES_NO_OPTION);
            if(result == JOptionPane.YES_OPTION)
            {
                Integer id = (Integer)this.tblGenres.getModel().getValueAt(row, 0);
                
                this.genreService.deleteGenereById(id);
                
                this.loadAllGenre();
            }
        }
    }
