    @Override
    public void tableChanged(TableModelEvent e) {
        int row = e.getFirstRow();
        int column = e.getColumn();
        if (e.getType() == TableModelEvent.UPDATE) {
            TableModel model = (TableModel) e.getSource();
            Integer id = (Integer)model.getValueAt(row, 0);
            String name = (String)model.getValueAt(row, 1);
            String gender = (String)model.getValueAt(row, 2);
            String phoneNo = (String)model.getValueAt(row, 3);
            
            
            Director director = new Director(id,name,gender,phoneNo);
            // Perform actions based on the updated data, e.g., update a database
            System.out.println("Cell updated at [" + row + ", " + column + "]: " );
            
            this.directorService.updateDirector(director);
            
        }
    }
