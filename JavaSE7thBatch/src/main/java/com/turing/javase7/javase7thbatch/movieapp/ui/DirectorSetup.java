    void deleteDirector()
    {
        int row = this.tblDirector.getSelectedRow();
        if(row !=-1)
        {
            int result = JOptionPane.showConfirmDialog(null, "Delete", "Are you sure you want to delete", JOptionPane.YES_NO_OPTION);
            if(result == JOptionPane.YES_OPTION)
            {
                Integer id = (Integer)this.tblDirector.getModel().getValueAt(row, 0);
                
                this.directorService.deleteDirectorById(id);
                
                this.loadAllDirectors();
            }
        }
    }
