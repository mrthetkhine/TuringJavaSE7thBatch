    void loadAllDirectors()
    {
        ArrayList<Director> directors = this.directorService.getAllDirector();
        System.out.println("directors size "+directors.size());
        DefaultTableModel model = (DefaultTableModel)this.tblDirector.getModel();
        
        model.setRowCount(0);
        
        for(Director director : directors)
        {
            Object[]row = new Object[4];
            row[0] = director.id();
            row[1] =director.name();
            row[2] = director.gender();
            row[3] = director.phoneNo();
            
            model.addRow(row);
        }
    }

